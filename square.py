def area(a):
    '''
    Вычисляет площадь квадрата по длине его стороны.

    Параметры:
        a (float): Длина стороны квадрата.

    Возвращаемое значение:
        float: Площадь квадрата.
    '''
    return a * a

def perimeter(a):
    '''
    Вычисляет периметр квадрата по длине его стороны.

    Параметры:
        a (float): Длина стороны квадрата.

    Возвращаемое значение:
        float: Периметр квадрата.
    '''
    return 4 * a
